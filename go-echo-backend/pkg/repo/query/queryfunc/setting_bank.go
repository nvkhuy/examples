package queryfunc

import (
	"text/template"

	"github.com/engineeringinflow/inflow-backend/pkg/db"
	"github.com/engineeringinflow/inflow-backend/pkg/helper"
	"github.com/engineeringinflow/inflow-backend/pkg/models"
)

type SettingBankAlias struct {
	*models.SettingBank
}

type SettingBankBuilderOptions struct {
	QueryBuilderOptions
}

func NewSettingBankBuilder(options SettingBankBuilderOptions) *Builder {
	var rawSQL = `
	SELECT /* {{Description}} */ st.*

	FROM setting_banks st
	`
	var countSQL = `
	SELECT /* {{Description}} */ 1

	FROM setting_banks st
	`

	return NewBuilder(rawSQL, countSQL).
		WithOptions(options, template.FuncMap{
			"Description": func() string {
				return helper.JoinNonEmptyStrings(
					"-",
					GetCaller(),
					options.Role.DisplayName(),
				)
			},
		}).
		WithPaginationFunc(func(db, rawSQL *db.DB) (interface{}, error) {
			var records = make([]*models.SettingBank, rawSQL.RowsAffected)

			rows, err := rawSQL.Rows()
			if err != nil {
				db.CustomLogger.Errorf("Scan rows error", err)
				return nil, err
			}
			defer rows.Close()

			for rows.Next() {
				var alias SettingBankAlias
				err = db.ScanRows(rows, &alias)
				if err != nil {
					db.CustomLogger.Errorf("Scan rows error", err)
					continue
				}

				records = append(records, alias.SettingBank)
			}

			return &records, nil
		})
}
